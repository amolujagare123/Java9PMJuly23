package ArraysDemo;

public class CharArrayDemo {

    public static void main(String[] args) {

        char[] ch = new char[5];

        ch[0] = 'j';
        ch[1] = 'k';
        ch[2] = 'l';
        ch[3] = 'm';
        ch[4] = 'n';

        for (char y:ch)
        {
            System.out.println(y);
        }
    }
}
