public class DataTypeDemo {

    public static void main(String[] args) {
// integer
        byte b = -128;
        short s = -32768;
        int n = 1787877787;
        long l = 5345678914343431234l;

// decimal
        float f = 1.3f;
        double d = 10.56;

        // boolean
        boolean b1 = true;
        boolean b2 = false;

        // character

        char c1  ='j';
        char c2  ='4';
        char c3  ='$';
    }

}
